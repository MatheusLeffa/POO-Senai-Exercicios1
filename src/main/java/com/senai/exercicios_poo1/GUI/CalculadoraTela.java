    private void btnResultadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadoActionPerformed
        float valor = Float.parseFloat(txtTelaResultado.getText());
        calculadora.setValor2(valor);
        String resultado = calculadora.resultado();
        txtTelaResultado.setText(resultado);
    }//GEN-LAST:event_btnResultadoActionPerformed

    private void digitarValoresTela(String digito) {
        if (txtTelaResultado.getText().equals("0")) {
            txtTelaResultado.setText("");
        }
        String valoresTela = txtTelaResultado.getText() + digito;
        txtTelaResultado.setText(valoresTela);
    }

    private void operacaoMatematica(String operacao) {
        float valor = Float.parseFloat(txtTelaResultado.getText());
        calculadora.setValor1(valor);
        calculadora.setOperador(operacao);
        txtTelaResultado.setText("0");
    }

