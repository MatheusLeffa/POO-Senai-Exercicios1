    private void btnApagarUltimoCharActionPerformed(java.awt.event.ActionEvent evt) {
        String valoresTela = txtTelaResultado.getText();

        if (!valoresTela.isEmpty()) {
            StringBuilder stringBuilder = new StringBuilder(txtTelaResultado.getText());
            stringBuilder.deleteCharAt(txtTelaResultado.getText().length() - 1);
            if (stringBuilder.toString().isEmpty()) {
                txtTelaResultado.setText("0");
            } else {
                txtTelaResultado.setText(stringBuilder.toString());
            }
        }
    }

